#Creating two sets
set1={1,2,3,4,5}
set2={4,5,6,7,8}

#Union
print("Union:", set1 | set2)

#Intersection
print("Intersection:", set1 & set2)

#Difference (elements in set1 not in set2)
print("Difference (set1 - set2):", set1 - set2)

#symmetric difference
print("Symmertic Difference:", set1 ^ set2)
